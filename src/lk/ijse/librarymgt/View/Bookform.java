    private void clearAll() {
        txt_bookid.setText("");
        txt_bookname.setText("");
//        txt_auther.setText("");
        txt_autherid.setText("");
        txt_isbn.setText("");
        txt_publisher.setText("");
        txt_publisherid.setText("");
        txt_catogory.setText("");
        txt_bookid.requestFocus();

    }

    private void getAllBooks() {
        try {

            dtm.setRowCount(0);
            ArrayList<BooksDTO> allBooks = BooksController.getAllBooks();
            for (BooksDTO cus : allBooks) {
                Object[] rowData = {cus.getBname(), cus.getBID(), cus.getAID(), cus.getIsbn(), cus.getPublisher(), cus.getPID(), cus.getCatogory()};
                dtm.addRow(rowData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Customerform.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Customerform.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private boolean deleteBooks(String BookID) throws ClassNotFoundException, SQLException {
        return BooksController.deleteBooks(BookID);
    }

    private void setBookID() {
        int id = new Random().nextInt(9999);
        String prefix = "B0";
        txt_bookid.setText(prefix + id);
    }

    private void loadAllCustomers() {
        cmbAuther.removeAll();
        try {
            ArrayList<AutherDTO> allAuthers = AutherController.getAllAuther();
            for (AutherDTO c : allAuthers) {
                cmbAuther.addItem(c.getAname());
            }

        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

