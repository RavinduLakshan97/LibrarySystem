    private void btn_addbookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addbookActionPerformed
        try {
            String BookID = txt_bookid.getText();
            String BookName = txt_bookname.getText();
//            String Auther = txt_auther.getText();
            String AID = txt_autherid.getText();
            String Isbn = txt_isbn.getText();
            String Publisher = txt_publisher.getText();
            String PID = txt_publisherid.getText();
            String Catogory = txt_catogory.getText();

            BooksDTO bookModel = new BooksDTO(BookID, BookName, AID, Isbn, Publisher, PID, Catogory);
            boolean addBooks = BooksController.addBooks(bookModel);
            if (addBooks) {
                clearAll();
                getAllBooks();
                JOptionPane.showMessageDialog(this, "Added");
            } else {
                JOptionPane.showMessageDialog(this, "Not Added");
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Bookform.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Bookform.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_addbookActionPerformed

